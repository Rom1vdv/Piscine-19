int	ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (nb < 0)
		return (0);
	while (a * a < nb)
		a++;
	if (a * a == nb)
		return (a);
	if (a >= 46341)
		return (0);
	return (0);
}
